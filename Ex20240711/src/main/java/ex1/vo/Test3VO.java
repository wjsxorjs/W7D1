package ex1.vo;

public class Test3VO {
	private String name;
	private int age;
	private boolean alive;
	
	public Test3VO(String name, int age, boolean alive) {
		this.name = name;
		this.age = age;
		this.alive = alive;
	}

	public String getName() {
		return name;
	}

	public int getAge() {
		return age;
	}

	public boolean isAlive() {
		return alive;
	}
	
	

}